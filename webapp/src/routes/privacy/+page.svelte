        저장해 다음 방문 때 같은 식당을 보여줍니다.
      </li>
      <li>
        <strong>식당 선택 기록</strong>
        메뉴 캐시와 사전 갱신 품질을 높이기 위해 선택된 식당 식별자와 선택 시각을 서버에
        기록할 수 있습니다. 이 기록은 계정, 이름, 이메일과 연결하지 않습니다.
      </li>
      <li>
        <strong>검색 및 메뉴 요청</strong>
        식당 검색어, 조회한 날짜와 식사 시간, 요청 URL은 검색 결과와 메뉴 페이지를 제공하기
        위해 처리됩니다.
      </li>
      <li>
        <strong>기기 및 접속 정보</strong>
        서버와 호스팅 환경은 보안, 장애 분석, 서비스 운영을 위해 IP 주소, 브라우저 정보,
        요청 시각 같은 기술 정보를 일시적으로 처리할 수 있습니다.
      </li>
      <li>
        <strong>사용자 상호작용 및 화면 스냅샷</strong>
        서비스 품질 개선과 오류 분석을 위해 방문한 페이지, 클릭, 마우스 이동, 스크롤,
        화면 크기, 사용 흐름, 페이지 화면 스냅샷이 수집될 수 있습니다. 화면 스냅샷에는
        캡처 시점에 페이지에 표시된 검색어, 식당명, 메뉴명, 선택 상태 같은 내용이 포함될 수
        있습니다.
      </li>
      <li>
        <strong>문의 정보</strong>
        이메일 또는 GitHub로 문의하면 답변에 필요한 주소, 계정명, 문의 내용을 처리합니다.
      </li>
    </ul>
